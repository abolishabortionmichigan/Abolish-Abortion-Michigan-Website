/**
 * Sync data/abolitionist-churches.json from the NXR church directory
 * (Church.json export). Filters to abolitionStance === 'pro_abolition'
 * and normalizes into the AAM schema.
 *
 * Usage:
 *   node scripts/sync-abolitionist-churches.mjs
 *   node scripts/sync-abolitionist-churches.mjs --source /path/to/Church.json
 *
 * Default source: ~/Downloads/Church.json (where the NXR export lands).
 */

import fs from 'node:fs';
import path from 'node:path';
import os from 'node:os';

const args = new Map();
for (let i = 2; i < process.argv.length; i += 2) {
  args.set(process.argv[i], process.argv[i + 1]);
}

const source = args.get('--source') || path.join(os.homedir(), 'Downloads', 'Church.json');
const destPath = path.join(process.cwd(), 'data', 'abolitionist-churches.json');

if (!fs.existsSync(source)) {
  console.error(`Source not found: ${source}`);
  process.exit(1);
}

const raw = JSON.parse(fs.readFileSync(source, 'utf-8'));
if (!Array.isArray(raw)) {
  console.error('Expected Church.json to be an array of records.');
  process.exit(1);
}

const proAbolition = raw.filter((c) => c && c.abolitionStance === 'pro_abolition');
console.log(`Found ${proAbolition.length} pro_abolition churches (of ${raw.length} total).`);

function slug(s) {
  return String(s || '')
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/^-+|-+$/g, '');
}

function pickNotes(c) {
  const parts = [];
  if (c.theologicalNotes) parts.push(c.theologicalNotes);
  if (c.description && !parts.some((p) => p.includes(c.description))) parts.push(c.description);
  const combined = parts.join(' ').trim();
  return combined || null;
}

const churches = proAbolition
  .map((c) => ({
    id: `${slug(c.name)}-${slug(c.city)}-${(c.state || 'xx').toLowerCase()}`,
    name: c.name,
    denomination: c.denomination || 'Unknown',
    address: [c.address, c.city, c.state, c.zip].filter(Boolean).join(', '),
    city: c.city,
    state: c.state || null,
    website: c.website || null,
    phone: c.phone || null,
    email: c.email || null,
    pastor: null, // Church.json has `leadership` but its shape isn't stable
    notes: pickNotes(c),
  }))
  .sort((a, b) => {
    const s = (a.state || '').localeCompare(b.state || '');
    if (s !== 0) return s;
    const cty = a.city.localeCompare(b.city);
    if (cty !== 0) return cty;
    return a.name.localeCompare(b.name);
  });

const out = {
  note:
    'Publicly-abolitionist churches from the NXR church directory ' +
    '(abolitionStance === "pro_abolition"). Regenerated by ' +
    'scripts/sync-abolitionist-churches.mjs. Do not hand-edit — ' +
    "changes should go into the NXR directory first, then re-sync.",
  syncedFrom: source.replace(os.homedir(), '~'),
  syncedAt: new Date().toISOString().slice(0, 10),
  totalScanned: raw.length,
  matched: churches.length,
  churches,
};

fs.writeFileSync(destPath, JSON.stringify(out, null, 2) + '\n', 'utf-8');
console.log(`Wrote ${destPath}  (${churches.length} entries)`);

// Quick breakdown by state
const byState = new Map();
for (const c of churches) {
  byState.set(c.state, (byState.get(c.state) || 0) + 1);
}
console.log('By state:');
for (const [s, n] of [...byState.entries()].sort((a, b) => b[1] - a[1])) {
  console.log(`  ${s}: ${n}`);
}
